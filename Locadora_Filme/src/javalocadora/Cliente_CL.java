    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
    ClienteDao cdao = new ClienteDao();
    Cliente c = new Cliente();
    String campo = "cpf";
    
    cdao.Consulta(campo, CL_CPF.getText(), c, jLabel1);
        
    if (CL_CPF.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o CPF");
        } else {
    CL_NOME.setText(""+c.getNome());
    CL_CPF.setText(c.getCpf());
    CL_RG.setText(c.getRg());
    CL_BAIRRO.setText(c.getBairro());
    CL_CELULAR_1.setText(c.getCelular1());
    CL_CELULAR_2.setText(c.getCelular2());    
    CL_CELULAR_3.setText(c.getCelular3());
    CL_CEP.setText(c.getCep());
    CL_CIDADE.setText(c.getCidade());
    CL_DATA_CADASTRO.setText(c.getData_cad());
    CL_DATA_NASCIMENTO.setText(c.getData_nasc());
    CL_EMAIL.setText(c.getEmail());
    CL_ESTADO.setText(c.getEstado());
    CL_ID.setText(String.valueOf(c.getId()));
    CL_RG.setText(c.getRg());
    CL_RUA.setText(c.getRua());
    CL_TELEFONE_FIXO.setText(c.getTel_fixo());
    CL_NUMERO.setText(c.getNum());
    CL_ID.setText(String.valueOf(c.getId()));
    }
    }//GEN-LAST:event_btn_pesquisarActionPerformed

