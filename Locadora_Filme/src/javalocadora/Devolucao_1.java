    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        LocacaoDao ldao = new LocacaoDao();
        Locacao f = new Locacao();
        String campo = "Cliente.cpf"; 
        
        
        
        List<Locacao> loc = ldao.getLista(campo, TXT_CPF.getText());
        DefaultTableModel model = (DefaultTableModel) TBL_CONS.getModel();
        model.setNumRows(0);

        for (Locacao locacao : loc) {
            model.addRow(new Object[]{
                locacao.getId_loc(),
                locacao.getTitulo(),
                locacao.getNome()});
                
            }
    }//GEN-LAST:event_jButton3ActionPerformed

