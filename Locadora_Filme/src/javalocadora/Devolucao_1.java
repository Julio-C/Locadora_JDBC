    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LocacaoDao ldao = new LocacaoDao();
        Locacao f = new Locacao();
        String campo = "id_loc";  
        
        try {
        ldao.Consulta(campo, txt_codloc.getText(), f);
          if (txt_codloc.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "DIGITE O CODIGO DA LOCAÇÃO !!!");
        } else {
              TXT_DEV.setText(f.getData_dev());
              TXT_VAL.setText(String.valueOf(f.getValor()));
              
          }
    } catch (SQLException ex) {  
        
    }      
    }//GEN-LAST:event_jButton1ActionPerformed

