    private void LocacaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LocacaoActionPerformed
                Locacao f = new Locacao();
                
        try {
                LocacaoDao ldao = new LocacaoDao();
                
                f.setTitulo(flm_1.getText());
                f.setData_dev(dt_dev.getText());
                f.setData_loc(dt_loc.getText());
                f.setId_cliente_pk(id_cli);
                f.setId_filme1_pk(id_film1);
                f.setId_filme2_pk(id_film2);
                f.setId_filme3_pk(id_film3);
                f.setId_filme4_pk(id_film4);
                f.setValor(Double.parseDouble(vlr_total.getText()));
                f.setStatus("Não Devolvido");
                ldao.Adiciona(f);
                
              
                         
        } catch (Exception e) {
            
        }
      
        


        JOptionPane.showMessageDialog(null,"Locação Cadastrado com Sucesso");
    }//GEN-LAST:event_LocacaoActionPerformed

