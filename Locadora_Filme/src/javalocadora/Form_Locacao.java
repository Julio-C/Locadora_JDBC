    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        FilmeDao fdao = new FilmeDao();
        Filme f = new Filme();
        String campo = "id_Filme";

    try {
        fdao.Consulta(campo, cod_1.getText(), f);
          if (cod_1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "DIGITE O CODIGO DO FILME !!!");
        } else {
            flm_1.setText(""+f.getTitulo());
            vl_1.setText(String.valueOf(f.getPreco()));
            id_film1 = f.getId_filme();
        
        }
    } catch (SQLException ex) {
        Logger.getLogger(Filme_FL.class.getName()).log(Level.SEVERE, null, ex);
    }     
    }//GEN-LAST:event_jButton2ActionPerformed

