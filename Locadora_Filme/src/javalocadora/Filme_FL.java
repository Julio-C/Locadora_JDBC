    private void btn_gravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_gravarActionPerformed
        Filme f = new Filme();
        try {
                f.setDuracao(FL_DURACAO.getText());
                f.setGenero(FL_GENERO.getSelectedItem().toString());
                f.setPreco(Double.parseDouble(FL_VALOR.getText()));
                f.setProdutora(FL_PRODUTORA.getText());
                f.setQtd(Integer.parseInt(FL_QUANTIDADE.getText()));
                f.setTitulo(FL_TITULO.getText());

        } catch (Exception e) {
            
        }
      
        FilmeDao cdao = new FilmeDao();
        cdao.Adiciona(f);
        JOptionPane.showMessageDialog(null,"Filme Cadastrado com Sucesso");
         
    }//GEN-LAST:event_btn_gravarActionPerformed

