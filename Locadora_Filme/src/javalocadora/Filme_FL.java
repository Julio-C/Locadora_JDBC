    private void btn_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_pesquisarActionPerformed
        FilmeDao fdao = new FilmeDao();
        Filme f = new Filme();
        String campo = "id_Filme";

    try {
        fdao.Consulta(campo, id.getText(), f);
          if (id.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "DIGITE O CODIGO DO FILME !!!");
        } else {
            FL_DURACAO.setText(f.getDuracao());
            FL_PRODUTORA.setText(f.getProdutora());
            FL_QUANTIDADE.setText(String.valueOf(f.getQtd()));
            FL_TITULO.setText(""+f.getTitulo());
            FL_VALOR.setText(String.valueOf(f.getPreco()));
            id.setText(String.valueOf(f.getId_filme()));
        }
    } catch (SQLException ex) {
        //Logger.getLogger(Filme_FL.class.getName()).log(Level.SEVERE, null, ex);
    }


    }//GEN-LAST:event_btn_pesquisarActionPerformed

